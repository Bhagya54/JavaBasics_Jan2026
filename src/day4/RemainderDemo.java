package day4;

public class RemainderDemo {
/*
 * You are given two numbers as input. You have to return the remainder when you divide
the first number by the second number.
 * 
 */
	
	
	public static void main(String[] args) {
		int a=100;
		int b=20;
		int remainder=a%b;
		System.out.println();
		
		System.out.println("Remainder is: " + remainder);
		
	}

}
